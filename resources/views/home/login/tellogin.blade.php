            var timea = null;
        }

        $('#hqcode').click(function(){

            var dqphone = $('#curphone').val();
            if(dqphone == ''){
                layui.use(['layer', 'form'], function(){
                  var layer = layui.layer
                  ,form = layui.form;
                  layer.msg('您的手机号没有填写');
                });
                return false;
            }

            $('#hqcode').prop('disabled',true); 
            $.post("/yzm", {    
               "_token": "{{ csrf_token() }}",
               'dqphone':dqphone
            }, function(data) {
               if(data.code == '1'){
                        layui.use(['layer', 'form'], function(){
                          var layer = layui.layer
                          ,form = layui.form;
                          out = 0;
                          layer.msg(data.msg);
                        });
               }else if(data.code == '0'){
                console.log(data);
                        layui.use(['layer', 'form'], function(){
                          var layer = layui.layer
                          ,form = layui.form;
                          out = 1;
                          layer.msg(data.msg);
                          $('#hqcode').prop('disabled',false); 
                        });
               }
            },'json');
            // $.ajaxSettings.async = true;
             setTimeout("pd()",1000);
            
        });
        function js()
        {
            console.log(a);
            if(a<1){
                $('#hqcode').prop('disabled',false); 
                $('#hqcode').val('获取验证码');
                clearInterval(timea);
                timea = null;
            }else{
                a -= 1;
                var txt = a + '秒可发送';
                $('#hqcode').val(txt);
                $('#hqcode').prop('disabled',true); 
            }
        }
        function pd()
        {
            if(out == 1){
                return false;
            }else if(out == null){
                layui.use(['layer', 'form'], function(){
                  var layer = layui.layer
                  ,form = layui.form;
                  out = null;
                  layer.msg('发送超时'); 
                });
                $('#hqcode').prop('disabled',false); 
                return false;
            }
            if(timea != null){
                return false;
            }else{
                a = 60;
                js();
                timea = setInterval('js()',1000);
            }
        }
